coco_gt = COCO("/content/dataset_split/test/annotations.json")

evaluator = CocoEvaluator(
    coco_gt,
    iou_types=["bbox"]
)

model_ft.model.eval()
model_ft.model.to(DEVICE)

for batch in tqdm(test_dl):
    with torch.no_grad():
        outputs = model_ft.model(
            pixel_values=batch["pixel_values"].to(DEVICE),
            pixel_mask=batch["pixel_mask"].to(DEVICE)
        )

    # ambil ukuran asli image (H, W)
    orig_sizes = torch.stack(
        [t["orig_size"] for t in batch["labels"]],
        dim=0
    )

    # POST PROCESS TANPA THRESHOLD (WAJIB UNTUK COCO)
    results = processor.post_process_object_detection(
        outputs,
        threshold=0.001,  # sangat kecil supaya semua prediksi masuk
        target_sizes=orig_sizes.to(DEVICE)
    )

    all_predictions_for_batch = []

    for t, r in zip(batch["labels"], results):
        image_id = t["image_id"].item()

        if r["boxes"].numel() == 0:
            continue

        for box, score, label in zip(r["boxes"], r["scores"], r["labels"]):
            x_min, y_min, x_max, y_max = box.tolist()
            bbox_coco = [x_min, y_min, x_max - x_min, y_max - y_min]

            all_predictions_for_batch.append({
                "image_id": image_id,
                "bbox": bbox_coco,
                "score": score.item(),
                "category_id": label.item(),
            })

    if len(all_predictions_for_batch) > 0:
        evaluator.update(all_predictions_for_batch)

evaluator.synchronize_between_processes()
evaluator.accumulate()
evaluator.summarize()

