class CocoDataset(CocoDetection):
    def __init__(self, img_dir, ann_file):
        super().__init__(img_dir, ann_file)

    def __getitem__(self, idx):
        image, target = super().__getitem__(idx)
        image_id = self.ids[idx]
        encoding = processor(
            images=image,
            annotations={"image_id": image_id, "annotations": target},
            return_tensors="pt"
        )
        return encoding["pixel_values"].squeeze(0), encoding["labels"][0]

def collate_fn(batch):
    pixel_values = [b[0] for b in batch]
    labels = [b[1] for b in batch]
    enc = processor.pad(pixel_values, return_tensors="pt")
    return {"pixel_values": enc["pixel_values"], "pixel_mask": enc["pixel_mask"], "labels": labels}

